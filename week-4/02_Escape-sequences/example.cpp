#include <iostream>

int main() {
    std::cout << "Hello, World!\n"; // newline
    std::cout << "Hello,\tWorld!\n"; // horizontal tab
    std::cout << "Hello, \"World!\"\n"; // double quote
    std::cout << "Hello,\\World!\n"; // backslash
    std::cout << "Hello, \aWorld!\n"; // alert (bell)

    return 0;
}
