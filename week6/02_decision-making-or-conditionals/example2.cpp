#include <iostream>

int main() {
    int num = -5;

    // Using if-else statement to check if a number is positive or negative
    if (num > 0) {
        std::cout << "Number is positive." << std::endl;
    } else {
        std::cout << "Number is non-positive (zero or negative)." << std::endl;
    }

    return 0;
}
